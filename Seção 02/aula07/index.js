/* OPERADORES ARITMÉTICOS
    => +
    => -
    => *
    => /
    => **
    => %
*/

let num1 = 255;
let num2 = 20;

console.log(num1 + num2);
console.log(num1 - num2);
console.log(num1 * num2);
console.log(num1 / num2);
console.log(num1 ** num2);
console.log(num1 % num2);

/* OPERADORES DE INCREMENTO
    => ++
    => --
*/

let contadorAdd = 0;
contadorAdd++;
contadorAdd++;
contadorAdd++;
console.log("Incremento:")
console.log(contadorAdd);

let contadorSub = 10;
contadorSub--;
contadorSub--;
contadorSub--;
contadorSub--;
contadorSub--;
contadorSub--;
console.log("Não incremento:")
console.log(contadorSub);